import xarray as xr

ds_wind = xr.open_dataset("era5_wind_2025.zip")
print(ds_wind)
print(ds_wind.data_vars)

import matplotlib.pyplot as plt
import cartopy.crs as ccrs
import cartopy.feature as cfeature

u = ds_wind["u10"]
v = ds_wind["v10"]

u_djf = u.groupby("valid_time.season").mean().sel(season="DJF")
v_djf = v.groupby("valid_time.season").mean().sel(season="DJF")

lon = ds_wind["longitude"]
lat = ds_wind["latitude"]

fig = plt.figure(figsize=(12, 8))
ax = plt.axes(projection=ccrs.PlateCarree())

ax.quiver(
    lon[::2],
    lat[::2],
    u_djf.values[::2, ::2],
    v_djf.values[::2, ::2],
    transform=ccrs.PlateCarree(),
    scale=150
)

ax.coastlines()
ax.add_feature(cfeature.BORDERS)

ax.set_title(
    "Figure 7.3 Atmospheric Moisture Transport Patterns\nSouth Caucasus (ERA5 2025, DJF)",
    fontsize=14
)

