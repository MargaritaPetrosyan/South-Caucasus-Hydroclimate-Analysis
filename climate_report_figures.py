"""#7.8 Computational Workflow for ERA5 Hydroclimatic Analysis"""

import matplotlib.pyplot as plt
from matplotlib.patches import FancyBboxPatch

steps = [
    "ERA5 Data\nDownload",
    "NetCDF\nExtraction",
    "Variable\nSelection",
    "Preprocessing\n& Cleaning",
    "Temporal\nAggregation",
    "Climate\nDiagnostics",
    "Visualization\n& Figures",
    "Report\nInterpretation"
]

fig, ax = plt.subplots(figsize=(14, 4))
ax.set_xlim(0, len(steps))
ax.set_ylim(0, 1)
ax.axis("off")

for i, step in enumerate(steps):
    box = FancyBboxPatch(
        (i + 0.1, 0.35),
        0.8,
        0.3,
        boxstyle="round,pad=0.04",
        linewidth=1.5,
        facecolor="white",
        edgecolor="black"
    )
    ax.add_patch(box)

    ax.text(
        i + 0.5,
        0.5,
        step,
        ha="center",
        va="center",
        fontsize=10
    )

    if i < len(steps) - 1:
        ax.annotate(
            "",
            xy=(i + 1.05, 0.5),
            xytext=(i + 0.9, 0.5),
            arrowprops=dict(arrowstyle="->", linewidth=1.5)
        )

plt.title(
    "Figure 7.8 Computational Workflow for ERA5 Hydroclimatic Analysis",
    fontsize=14
)

plt.savefig(
    "figure_7_8_computational_workflow.png",
    dpi=300,
    bbox_inches="tight"
)

plt.show()

